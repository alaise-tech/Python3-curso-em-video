#Operadores Aritmeticos
n1 = int(input("Digite um número:"))
n2 = int(input("Digite outro número:"))
soma = n1 + n2
subtração = n1 - n2
multiplicação = n1 * n2
divisão = n1 / n2
print("A soma é {}, a subtração é {}, a multiplicação é {}, a divisão é {:.3f}".format(soma, subtração, multiplicação, divisão), end=' ')